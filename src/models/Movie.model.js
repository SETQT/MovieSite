


const Movie = class {

    constructor(id, img, title, year, topRank, rating, ratingCount, gentres, reviews, synopses, casts) {
        this.id = id;
        this.img = img;
        this.title = title;
        this.year = year;
        this.topRank = topRank;
        this.rating = rating;
        this.ratingCount = ratingCount;
        this.gentres = gentres;
        this.reviews = reviews;
        this.synopses = synopses;
        this.casts = casts;
    }



}

module.exports = Movie